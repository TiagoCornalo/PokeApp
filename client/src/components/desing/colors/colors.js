export default function styleCard (type) {
  switch (type) {
    case 'bug':
      return '#26de81'
    case 'dragon':
      return '#ffeaa7'
    case 'electric':
      return '#fed330'
    case 'fairy':
      return '#FF0069'
    case 'fighting':
      return '#30336b'
    case 'fire':
      return '#f0932b'
    case 'flying':
      return '#81ecec'
    case 'grass':
      return '#00b894'
    case 'ground':
      return '#EFB549'
    case 'ghost':
      return '#a55eea'
    case 'ice':
      return '#74b9ff'
    case 'normal':
      return '#95afc0'
    case 'poison':
      return '#6c5ce7'
    case 'psychic':
      return '#a29bfe'
    case 'rock':
      return '#2d3436'
    case 'water':
      return '#0190FF'
  }
}
